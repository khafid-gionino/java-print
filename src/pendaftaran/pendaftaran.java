    private void kosongkanfrom(){
        id_pendaftaran.setText(null);
        id_pelanggan.setSelectedItem(null);
        nama.setText(null);
        nomor_kamar.setSelectedItem(null);
        fasilitas.setText(null);
        harga.setText(null);
        tgl_daftar.setText(null);
    }
    
    private void tambah(){
        try{
            object.cpendaftaran.insertObj( id_pendaftaran.getText(), nama.getText(),(String) nomor_kamar.getSelectedItem(), fasilitas.getText(), harga.getText(), tgl_daftar.getText());
            object.cpendaftaran.insertDB(id_pendaftaran.getText(), (String)id_pelanggan.getSelectedItem(), (String)nomor_kamar.getSelectedItem(), tgl_daftar.getText());
            
            JOptionPane.showMessageDialog(this, "Tambah data berhasil !");
            kosongkanfrom();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Tambah data gagal ??"+e);
        }
    }
    
    private void hapus(){
        try{
            int index = tbpendaftaran.getSelectedRow();
             if (index == -1){
             return;
             }
             modelpendaftaran mp = object.cpendaftaran.getModelPendaftaran(index);
             object.cpendaftaran.delete(index);
             object.cpendaftaran.deleteDB(mp);
             
             JOptionPane.showMessageDialog(this, "Data Berhasil Di hapus !");
            kosongkanfrom();
            tbpendaftaran.clearSelection();
         cp.delete(index);
        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "Hapus Data GagaL ??"+e);
        }
    }

