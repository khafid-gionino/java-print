    private void nomor_kamarItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_nomor_kamarItemStateChanged
        // TODO add your handling code here:
        try {
            Connection con = koneksi.getConnection();
            Statement stt = con.createStatement();
            String sql = "select fasilitas, harga from kamar where id_kamar ="+nomor_kamar.getSelectedItem();
            ResultSet res = stt.executeQuery(sql);

            while(res.next()){
                Object[] ob = new Object[3];
                ob[0] = res.getString(1);
                ob[1] = res.getString(2);

                fasilitas.setText((String) ob[0]);
                harga.setText((String) ob[1]);
            }
            res.close();
            stt.close();
        } catch (Exception e) {

        }
    }//GEN-LAST:event_nomor_kamarItemStateChanged

