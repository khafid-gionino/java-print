    private void id_pendaftaranItemStateChanged(java.awt.event.ItemEvent evt) {                                               
        try{
            Connection con= koneksi.getConnection();
            Statement stt = con.createStatement();
            String sql = "select nama,no_kamar from pendaftaran where id_pendaftaran ="+id_pendaftaran.getSelectedItem();
            ResultSet res = stt.executeQuery(sql);
        
            while (res.next()){
                Object[] ob = new Object[3];
                ob[0] = res.getString(1);
                ob[1] = res.getString(2);
                
                nama.setText((String) ob[0]);
                no_kamar.setText((String) ob[1]);
//                ob[0]=res.getString((String) ob[0]);
                id_pendaftaran.addItem((String) ob[0]);
            }
            res.close();
            stt.close();
        } catch (Exception e){
        }
    } 
    
    private void loadCombo() {
         try {
            Connection con = koneksi.getConnection();
            Statement stt = con.createStatement();
            String sql = "select id_pendaftaran from pendaftaran";
            ResultSet res = stt.executeQuery(sql);
            
            while(res.next()){
            Object[] ob = new Object[3];
            ob[0] = res.getString(1);
            
            id_pendaftaran.addItem((String) ob[0]);
        }
        res.close(); 
        stt.close();
        } catch (Exception e) {
            
        }
    }
    
