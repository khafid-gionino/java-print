    private void kosongkanfrom(){
        id_transaksi.setText(null);
        id_pendaftaran.setSelectedItem(null);
        nama.setText(null);
        no_kamar.setText(null);
        fasilitas.setText(null);
        harga.setText(null);
        tgl.setText(null);
        status.setText(null);
        bayar.setText(null);
    }
    
    private void tambah(){
        try{
            object.ctransaksi.insertObj(id_transaksi.getText(), (String) id_pendaftaran.getSelectedItem(), nama.getText(), no_kamar.getText(), fasilitas.getText(), harga.getText(), bayar.getText(), tgl.getText(), status.getText());
            object.ctransaksi.insertDB(id_transaksi.getText(),  (String)id_pendaftaran.getSelectedItem(), no_kamar.getText(), status.getText(), tgl.getText());
            
            JOptionPane.showMessageDialog(this, "Tambah data berhasil !!");
            kosongkanfrom();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Tambah data gagal ?? "+e);
        }
    }
    
    private void hapus(){
        try{
            int index = tbtransaksi.getSelectedRow();
             if (index == -1){
             return;
             }
             modeltransaksi mp = object.ctransaksi.getModelTransaksi(index);
             object.cpendaftaran.delete(index);
             
             JOptionPane.showMessageDialog(this, "Data Berhasil Di hapus !");
            kosongkanfrom();
            tbtransaksi.clearSelection();
         ct.delete(index);
        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "Hapus Data GagaL ??"+e);
        }
    }
    
